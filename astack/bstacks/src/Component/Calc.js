export const add = (a,b) =>{
    let sum = a+b;
    return sum;
}

export const sub = (a,b) =>{
    let res = a-b;
    return res;
}

export const mul = (a,b) =>{
    let res = a*b;
    return res;
}

export const div = (a,b) =>{
    let res = a/b;
    return res;
}

